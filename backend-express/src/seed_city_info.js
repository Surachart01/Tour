async function main() {
  console.log("Seeding City Information...");
  for (const city of initialCities) {
    await prisma.cityInfo.upsert({
      where: { cityName: city.cityName },
      update: city,
      create: city
    });
  }
  console.log("City Information seeded successfully!");
}

main()
  .catch(err => {
    console.error(err);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
